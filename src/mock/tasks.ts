export const getTasksByStatus = (status: string): Task[] => {
  return mockTasks.filter((t) => t.status === status);
};

export const getTasksByPriority = (priority: string): Task[] => {
  return mockTasks.filter((t) => t.priority === priority);
};

export const getTaskById = (id: string): Task | undefined => {
  return mockTasks.find((t) => t.id === id);
};

export const getPendingTasks = (): Task[] => {
  return mockTasks.filter((t) => t.status === "pending" || t.status === "processing");
};

export const getTodayTasks = (): Task[] => {
  return mockTasks.filter((t) => {
    const today = new Date("2026-06-16");
    const triggerDate = new Date(t.triggerTime);
    return triggerDate.toDateString() === today.toDateString();
  });
};
